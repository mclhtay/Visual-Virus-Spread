package com.carter.visualvirusspread;

public class Virus {

	private String name;
	private int radius;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getRadius() {
		return radius;
	}

	public void setRadius(int radius) {
		this.radius = radius;
	}

	public Virus() {
		
	}
}
